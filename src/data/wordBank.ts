/**
 * Returns a shuffled list of words filtered by difficulty, excluding recently used ones.
 */
export function getWordList(
  difficulty: WordDifficulty,
  count: number,
  exclude: Set<string> = new Set(),
): string[] {
  const allCategories = Object.keys(WORD_BANK) as WordCategory[]
  const pool: string[] = []

  for (const category of allCategories) {
    for (const entry of WORD_BANK[category]) {
      if (entry.difficulty === difficulty && !exclude.has(entry.word)) {
        pool.push(entry.word)
      }
    }
  }

  // Fallback: if not enough after exclusion, include excluded ones
  if (pool.length < count) {
    for (const category of allCategories) {
      for (const entry of WORD_BANK[category]) {
        if (entry.difficulty === difficulty && !pool.includes(entry.word)) {
          pool.push(entry.word)
        }
      }
    }
  }

  // Shuffle (Fisher-Yates)
  for (let i = pool.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[pool[i], pool[j]] = [pool[j]!, pool[i]!]
  }

  return pool.slice(0, count)
}

/**
 * Returns a word list from a single random category.
 */
export function getWordListByCategory(
  difficulty: WordDifficulty,
  count: number,
  exclude: Set<string> = new Set(),
): string[] {
  const allCategories = Object.keys(WORD_BANK) as WordCategory[]
  const shuffledCategories = [...allCategories].sort(() => Math.random() - 0.5)

  for (const category of shuffledCategories) {
    const pool = WORD_BANK[category]
      .filter((e) => e.difficulty === difficulty && !exclude.has(e.word))
      .map((e) => e.word)

    if (pool.length >= count) {
      for (let i = pool.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1))
        ;[pool[i], pool[j]] = [pool[j]!, pool[i]!]
      }
      return pool.slice(0, count)
    }
  }

  // Fallback to cross-category
  return getWordList(difficulty, count, exclude)
}
